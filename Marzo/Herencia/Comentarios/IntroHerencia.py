def suma (a,b):     #Se define la funcion "suma" con dos parametros "a" y "b"
    return a+b      #se retornan dichos valores con una operacion de suma

def suma(a,b,c):    #definicion de funcion con tres parametros "a", "b" y "c"
    return a+b+c    #retorno de los valores sumados entre si

print(suma(1,2,3))  #Impresion de la funcion con asignacion de valores enteros.
